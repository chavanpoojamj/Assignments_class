def add(num1,num2):
    return num1+num2

def sub(num1,num2):
    return num1-num2

def mul(num1,num2):
    return num1*num2

def div(num1,num2):
    return num1/num2


number1=int(input("Enter 1st number:"))
number2=int(input("Enter 2nd number:"))

result1=add(number1,number2)
print(result1)

result2=sub(number1,number2)
print(result2)

result3=mul(number1,number2)
print(result3)

result4=div(number1,number2)
print(result4)



